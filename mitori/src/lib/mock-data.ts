function interleaveByGenre(items: Content[]): Content[] {
  const groups = new Map<string, Content[]>()
  for (const item of items) {
    if (!groups.has(item.genre.id)) groups.set(item.genre.id, [])
    groups.get(item.genre.id)!.push(item)
  }
  const cols = Array.from(groups.values())
  const result: Content[] = []
  const maxLen = Math.max(...cols.map(c => c.length))
  for (let i = 0; i < maxLen; i++) {
    for (const col of cols) {
      if (i < col.length) result.push(col[i])
    }
  }
  return result
}

