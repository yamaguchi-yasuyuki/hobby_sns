function makeContent(
  id: string,
  title: string,
  description: string,
  seed: string,
  photoCount: number,
  sourceType: Content["source_type"],
  genreObj: Content["genre"],
  locationName: string | null,
  prefecture: string | null,
  bookmarkCount: number,
  viewCount: number,
  isBookmarked = false,
): Content {
  const mainImage = REAL_IMAGES[genreObj.slug] ?? img(`${seed}-a`)
  const sample = SAMPLE[id]
  return {
    id,
    title,
    description,
    body: sample?.body,
    image_url: mainImage,
    images: [
      mainImage,
      ...Array.from({ length: photoCount - 1 }, (_, i) =>
        img(`${seed}-${"bcde"[i]}`)
      ),
    ],
    external_url: "https://note.com",
    source_type: sourceType,
    links: sample?.links,
    genre: genreObj,
    location_name: locationName,
    prefecture,
    bookmark_count: bookmarkCount,
    view_count: viewCount,
    status: "published",
    is_bookmarked: isBookmarked,
    created_at: `2026-04-${String(Number(id) % 28 + 1).padStart(2, "0")}T10:00:00+09:00`,
  }
}

